class Main {
    public static void main(String[] args) {
      
      // declare variables
      double a = 23, b = 13;
  
      // addition operator
      System.out.println("a + b = " + (a + b));
  
      // subtraction operator
      System.out.println("a - b = " + (a - b));
  
      // multiplication operator
      System.out.println("a * b = " + (a * b));
  
      // division operator
      System.out.println("a / b = " + (a / b));
  
      // modulo operator
      System.out.println("a % b = " + (a % b));
    }
  }
